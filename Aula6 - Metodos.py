conjunto = {1,2,3,4,5}
conjunto2 = {5,1,8,9,10}

#uniao de conjuntos
uniConj = conjunto.union(conjunto2)
print(uniConj)

print('..........interseção')

conj_ntersessao = conjunto.intersection(conjunto2)
print(conj_ntersessao)
#diferença

conj_diferent =  conjunto.difference(conjunto2)
print(conj_diferent)



